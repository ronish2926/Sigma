package com.haris.kareem.ObjectUtil;

public class ColorObject {
    private int startColour;
    private int endColour;


    public int getStartColour() {
        return startColour;
    }

    public ColorObject setStartColour(int startColour) {
        this.startColour = startColour;
        return this;
    }

    public int getEndColour() {
        return endColour;
    }

    public ColorObject setEndColour(int endColour) {
        this.endColour = endColour;
        return this;
    }
}
